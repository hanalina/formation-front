// INCREMENTATION ET DECREMENTATION //

// INCREMENTATION

var nb1 = 1;
nb1 = nb1+1;
// ecriture simplifiée
nb1++; // le ++ incremente toujours de 1, c'est le raccourci du dessus, c'est un par pas de 1

// DECREMENTATION
nb1 = nb1-1;
//ecriture simplifiée
nb1-- // par pas de 1 comme au dessus

// quand ++ apres, il affcihe dans la console d'avant d'incrementer, quand le ++ avant, il affiche directement l'incrementation